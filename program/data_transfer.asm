; data_transfer.asm
; 演示数据在寄存器和内存之间的搬运 (修改后避免溢出)

LDI R1, 0x50    ; 将立即数 0x50 加载到 R1
STORE R1, [0x30]; 将 R1 的内容 (0x50) 存储到内存地址 0x30

LDI R2, 0x30    ; 将立即数 0x30 加载到 R2
STORE R2, [0x31]; 将 R2 的内容 (0x30) 存储到内存地址 0x31

LOAD R3, [0x30] ; 从内存地址 0x30 加载数据到 R3 (R3 应为 0x50)
LOAD R4, [0x31] ; 从内存地址 0x31 加载数据到 R4 (R4 应为 0x30)

ADD R5, R3, R4  ; R5 = R3 + R4 (0x50 + 0x30 = 0x80)
STORE R5, [0x32]; 将 R5 的内容 (0x80) 存储到内存地址 0x32

; 结束程序
LDI R0, 0
JRZ R0, -1      ; 无限循环
